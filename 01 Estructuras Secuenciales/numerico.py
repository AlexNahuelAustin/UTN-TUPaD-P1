numero_decimal=input("ingrese un numero decimal: ")
#convertimos el numero ingresado a unos decimal con float.
numero_decimal=float(numero_decimal)
#mostramos el numero decimal por terminal.
print("el numero decimal ingresdao es: ",numero_decimal )
#sumamos ambos numeros.
suma=numero_decimal+numero_entero
